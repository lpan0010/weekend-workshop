package Payslip;

public class Employee {
	public String name;
    public String surname;
    public Double salary;
    public Double superRate;
    public String month;

    public Employee(String name, String surname, Double salary, Double superRate, String month)
    {
        this.name = name;
        this.surname = surname;
        this.salary = salary;
        this.superRate = superRate;
        this.month = month;
    }
    

}
